package eu.printingin3d.javascad.enums;

/**
 * Represents the relation of a point and an area.
 */
public enum PointRelation {
    /**
     * The point is inside of the area.
     */
    INSIDE,
    /**
     * The point is on the border of the area.
     */
    BORDER,
    /**
     * The point is outside of the area.
     */
    OUTSIDE;
}